// scripts/build-help-articles.js
//
// Reads docs/HELP_ARTICLES/*.md, parses YAML frontmatter, emits
// src/data/helpArticles.js with all article metadata + body content
// inlined as a JS array. Runs at build time via the 'prebuild' npm
// script in package.json.
//
// This is the bridge between the help corpus (markdown files in docs)
// and the runtime bundle (the React app). Editing an article means:
//   1. Edit the markdown file in docs/HELP_ARTICLES/
//   2. Push to main
//   3. Vercel rebuild runs this script automatically
//   4. Updated article ships to clients
//
// We bundle at build time rather than fetching at runtime because:
//   - Help articles are static content; no need for the GitHub-fetch
//     pattern used by the Founder Hub (which is internal-only and
//     wants live updates without redeploy)
//   - Bundling means search is instant; no network round-trip
//   - Bundling means the help center works offline once the page
//     has loaded, matching the PWA behavior we already have

const fs = require('fs');
const path = require('path');

const ARTICLES_DIR = path.join(__dirname, '..', 'docs', 'HELP_ARTICLES');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'data', 'helpArticles.js');

function parseFrontmatter(text) {
  const match = text.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!match) return { frontmatter: {}, body: text };

  const frontmatterText = match[1];
  const body = match[2];
  const frontmatter = {};

  // Simple YAML-ish parser. Handles key: value pairs only.
  // Sufficient for our frontmatter format.
  for (const line of frontmatterText.split('\n')) {
    const colonIdx = line.indexOf(':');
    if (colonIdx === -1) continue;
    const key = line.slice(0, colonIdx).trim();
    let value = line.slice(colonIdx + 1).trim();
    // Strip quotes if present
    if (value.startsWith('"') && value.endsWith('"')) {
      value = value.slice(1, -1);
    }
    // Convert numeric-looking values
    if (key === 'order' && !isNaN(parseInt(value, 10))) {
      value = parseInt(value, 10);
    }
    frontmatter[key] = value;
  }

  return { frontmatter, body: body.trim() };
}

function main() {
  if (!fs.existsSync(ARTICLES_DIR)) {
    console.error(`Help articles directory not found: ${ARTICLES_DIR}`);
    process.exit(1);
  }

  const files = fs.readdirSync(ARTICLES_DIR).filter((f) => f.endsWith('.md'));
  console.log(`Building help articles from ${files.length} markdown files...`);

  const articles = files.map((filename) => {
    const filepath = path.join(ARTICLES_DIR, filename);
    const text = fs.readFileSync(filepath, 'utf8');
    const { frontmatter, body } = parseFrontmatter(text);

    if (!frontmatter.id) {
      console.warn(`Skipping ${filename}: missing id in frontmatter`);
      return null;
    }

    return {
      id: frontmatter.id,
      title: frontmatter.title || frontmatter.id,
      category: frontmatter.category || 'Other',
      order: typeof frontmatter.order === 'number' ? frontmatter.order : 999,
      keywords: frontmatter.keywords || '',
      taxonomy: frontmatter.taxonomy || '',
      body: body,
    };
  }).filter(Boolean);

  // Sort by category, then order within category
  articles.sort((a, b) => {
    if (a.category !== b.category) return a.category.localeCompare(b.category);
    return a.order - b.order;
  });

  // Write the bundled data file
  const jsContent = `// AUTO-GENERATED FILE. Do not edit by hand.
// Source: docs/HELP_ARTICLES/*.md
// Generated by: scripts/build-help-articles.js
// To update articles, edit the markdown files and rebuild.

const HELP_ARTICLES = ${JSON.stringify(articles, null, 2)};

export default HELP_ARTICLES;
`;

  // Ensure output directory exists
  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_FILE, jsContent, 'utf8');
  console.log(`Built ${articles.length} help articles to ${OUTPUT_FILE}`);

  // Print summary
  const categories = {};
  for (const a of articles) {
    categories[a.category] = (categories[a.category] || 0) + 1;
  }
  console.log('Categories:');
  for (const [cat, count] of Object.entries(categories)) {
    console.log(`  ${cat}: ${count}`);
  }
}

main();
